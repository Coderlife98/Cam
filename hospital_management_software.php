  <script>
    document.addEventListener('DOMContentLoaded', function () {
      const questions = document.querySelectorAll('.question');

      questions.forEach(question => {
        question.addEventListener('click', function () {
          const answer = this.nextElementSibling;
          const activeAnswer = document.querySelector('.faq-item.active .answer');

          // Hide the previously shown answer (if any)
          if (activeAnswer) {
            activeAnswer.classList.remove('active');
          }

          // Toggle the visibility of the clicked answer
          answer.classList.toggle('active');
        });
      });
    });
  </script>
  <script>

    document.addEventListener('DOMContentLoaded', function () {
      const testimonials = document.querySelectorAll('.testimonial');
      let currentIndex = 0;

      function showTestimonial(index) {
        testimonials.forEach((testimonial, i) => {
          if (i === index) {
            testimonial.classList.add('active');
          } else {
            testimonial.classList.remove('active');
          }
        });
      }

      function nextTestimonial() {
        currentIndex++;
        if (currentIndex >= testimonials.length) {
          currentIndex = 0;
        }
        showTestimonial(currentIndex);
      }

      testimonials.forEach((testimonial, index) => {
        testimonial.addEventListener('click', () => {
          currentIndex = index;
          showTestimonial(currentIndex);
        });
      });

      setInterval(nextTestimonial, 4000); // Auto-slide every 5 seconds
    });

  </script>

